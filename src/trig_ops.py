import math

class TrigonometricCalculator:
    """Handles trigonometric operations."""
    def sin(self, x: float) -> float:
        """Returns the sine of x (in radians)."""
        return math.sin(x)
    
    def cos(self, x: float) -> float:
        """Returns the cosine of x (in radians)."""
        return math.cos(x)
